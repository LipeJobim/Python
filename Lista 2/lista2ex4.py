# 4. Escreva um algoritmo para ler 2 valores (considere que não serão informados valores iguais) e escreva o maior deles.

valor1 = int(input("Qual é o valor 1?"))
valor2 = int(input("Qual é o valor 2?"))
if valor1 > valor2:
    print("Valor 1 é maior:", valor1)
else:
    print("Valor 2 é maior:", valor2)
